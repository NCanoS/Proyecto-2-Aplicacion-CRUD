class Equipos {
    nombre ="";
    marca = "";
    ubicacion = "";
    cantidad = 0;
    comentarios = "";
    operacional = false;

    constructor(nombre, marca, ubicacion, cantidad, comentarios, operacional){
        this.nombre=nombre;
        this.marca=marca;
        this.ubicacion=ubicacion;
        this.cantidad=cantidad;
        this.comentarios=comentarios;
        this.operacional=operacional;
    }
}